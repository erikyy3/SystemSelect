    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        int confirmacao = JOptionPane.showConfirmDialog(null, "Tem certeza que deseja excluir o item?", "Excluir Item?", JOptionPane.YES_NO_OPTION);
                
        if(confirmacao == JOptionPane.YES_OPTION){       
        int itemSelecionado = tblItem.getSelectedRow();
        
        if(itemSelecionado != -1){
            int idItem = Integer.parseInt(tblItem.getValueAt(itemSelecionado, 0).toString());
            
            ComputadorService service = new ComputadorService();
            boolean verificacao = service.excluirItem(idItem);
            
            if(verificacao == true){
               JOptionPane.showMessageDialog(null, "Item excluído com sucesso.");
               String nome = txtNome.getText();
                String marca = txtMarca.getText();
                String precoInicial = txtValorIni.getText();
                String precoFinal = txtValorFinal.getText();
                List<Computador> itens = service.listarItens(nome, marca, precoInicial, precoFinal);
                preencherTabela(itens);
            }
            
            else{
                JOptionPane.showMessageDialog(null, "Falha ao excluir item."); //caso haja alguma falha no momento de exclusão, essa mensagem aparecerá.
            }
        }else{ 
            JOptionPane.showMessageDialog(null, "Selecione um item para excluir."); //caso o usuário clique sim mas não tenha nenhum item selecionado, esta mensagem aparecerá.
        }
        }else{ 
          JOptionPane.showMessageDialog(null, "Operação cancelada!"); // caso o usuário clicar não, a operação de exclusão será cancelada.
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

