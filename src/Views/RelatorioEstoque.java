    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        int computadorSelecionado = tblItem.getSelectedRow();
        
        if(computadorSelecionado != -1){
        int idComputador =Integer.parseInt(tblItem.getValueAt(computadorSelecionado, 0).toString());
        ComputadorService service = new ComputadorService();
        Computador computadorEncontrado = service.encontrarItem(idComputador);
        
        if(computadorEncontrado != null){ //caso o computador for encontrado no banco de dados.
                EdicaoItem telaEdicaoItem = new EdicaoItem(computadorEncontrado);
                telaEdicaoItem.setVisible(true); // janela de EdicaoItem sendo aberta.
                dispose(); //Janela atual sendo fechada.  
        }
        }else{
            JOptionPane.showMessageDialog(null, "Selecione um item para editar."); //caso for clicado no botão "Editar" porém nenhum item da tabela for selecionado, essa mensagem aparecerá
        }
    }//GEN-LAST:event_btnEditarActionPerformed

