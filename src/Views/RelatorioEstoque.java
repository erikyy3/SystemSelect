    public void preencherTabela(List<Computador> computadores){ //método para preencher a tabela de itens.
        String colunas[] = {"Id", "Nome", "Descrição", "Tipo", "Marca", "Preço", "Quantidade"};
        String dados[][] = new String[computadores.size()][colunas.length];
        
        int i = 0;
        for(Computador c : computadores){
            dados[i] = new String[]{
                String.valueOf(c.getId()),
                c.getNome(),
                c.getDescricao(),
                c.getTipo(),
                c.getMarca(),
                String.valueOf(c.getPreco()),
                String.valueOf(c.getQuantidade())
            };
            
            i++;
        }

    DefaultTableModel model = new DefaultTableModel(dados, colunas);
    tblItem.setModel(model);
    }
    
