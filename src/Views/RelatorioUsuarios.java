public void preencherTable(List<Usuario> usuarios){
    String colunas[] = {"Id", "Nome", "Senha", "Email", "Número Cartão"};
    String dados[][] = new String[usuarios.size()][colunas.length];
    
    int i = 0;
    for(Usuario u : usuarios){
        dados[i] = new String[]{
        String.valueOf(u.getId()),
        u.getNome(),
        u.getSenha(),
        u.getEmail(),
        u.getNumCartao()
    };
        i++;
    }
    DefaultTableModel model = new DefaultTableModel(dados, colunas);
    tblUsuario.setModel(model);
}
