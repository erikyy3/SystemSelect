    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        UsuarioService service = new UsuarioService();
        
        int usuarioSelecionado = tblUsuario.getSelectedRow();
        if(usuarioSelecionado != -1){
            int idUsuario = Integer.parseInt(tblUsuario.getValueAt(usuarioSelecionado, 0).toString());
            
            boolean verificacao = service.excluirUsuario(idUsuario);
            if(verificacao == true) {
                JOptionPane.showMessageDialog(null, "Usuário excluído com sucesso.");
            }
            else{
               JOptionPane.showMessageDialog(null, "Erro ao excluir usuário"); 
            }
            
            String nome = txtUsuario.getText();
            List<Usuario> usuarios = service.listarClientesPorNome(nome);
            preencherTable(usuarios);
        }
        else{
            JOptionPane.showMessageDialog(null, "Selecione um usuário para excluir.");
        }
        
        
    }//GEN-LAST:event_btnExcluirActionPerformed

    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        int usuarioSelecionado = tblUsuario.getSelectedRow();
       
        if(usuarioSelecionado != -1){
            int idUsuario = Integer.parseInt(tblUsuario.getValueAt(usuarioSelecionado, 0).toString());
            UsuarioService service = new UsuarioService();
            Usuario usuarioEncontrado = service.encontrarUsuario(usuarioSelecionado);
        
                if(usuarioEncontrado != null){
                    EdicaoUsuario telaEdicao = new EdicaoUsuario(usuarioEncontrado);
                    telaEdicao.setVisible(true);
                    dispose();
                }
        
                else{
                    JOptionPane.showMessageDialog(null, "Usuário não encontrado.");
                }
        
        }
        else{
            JOptionPane.showMessageDialog(null, "Selecione um usuário para editar.");
        }
    }//GEN-LAST:event_btnEditarActionPerformed

