    private void btnPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPesquisarActionPerformed
        
        VendaService service = new VendaService();
        List<Venda> vendas = service.listarTodasVendasDATA(txtDataInicial.getText(), txtDataFinal.getText()); //busca todos as vendas listadas no intervalo de tempo informado.
        
        if(vendas == null){
            JOptionPane.showMessageDialog(this, "Falha na busca, por favor informar no valor correto.(dd/MM/yyyy)"); //caso falha na busca entre o intervalo de tempo, este erro aparecerá.
        }
        else{
            preencherTabela(vendas);
        }
    }//GEN-LAST:event_btnPesquisarActionPerformed

