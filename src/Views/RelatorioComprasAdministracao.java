    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        int vendaSelecionada = tblVenda.getSelectedRow();
        
        VendaService service = new VendaService();
        
        if(vendaSelecionada != -1){
            int idVenda = Integer.parseInt(tblVenda.getValueAt(vendaSelecionada, 0).toString()); //idVenda = ao número da linha selecionada
            
            Venda vendaEncontrada = service.encontrarVenda(idVenda);
            
            EdicaoVenda telaEdicao = new EdicaoVenda(vendaEncontrada); //abre a view EdicaoVenda com os dados encontrados da venda/linha selecionada
            telaEdicao.setVisible(true); 
            dispose(); //e fecha a janela atual
        }
        
        else{
            JOptionPane.showMessageDialog(null, "Selecione uma venda para editar.");// caso nenhuma venda for selecionada e for apertado o botão de editar, aparecerá esse aviso
        }
    }//GEN-LAST:event_btnEditarActionPerformed

