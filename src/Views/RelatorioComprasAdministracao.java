    private void bntExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bntExcluirActionPerformed
        VendaService service = new VendaService();
        int vendaSelecionada = tblVenda.getSelectedRow();
        if(vendaSelecionada != -1){
            int idVenda = Integer.parseInt(tblVenda.getValueAt(vendaSelecionada, 0).toString()); //idVenda = ao número da linha selecionada
            
            int confirmacao = JOptionPane.showConfirmDialog(null, "Você tem certeza que deseja excluir esta venda?", "Confirmação", JOptionPane.YES_NO_OPTION); //mensagem de confirmação se realmente deseja excluir a venda selecionado.
            
            if(confirmacao == JOptionPane.YES_OPTION){ //caso clicar em sim/yes, a venda será excluída
            service.excluirVenda(idVenda);
            JOptionPane.showMessageDialog(null, "Venda excluída com sucesso.");
            List<Venda> vendas = service.listarTodasVendas();
            preencherTabela(vendas);
            }
            else{
                JOptionPane.showMessageDialog(null, "Operação cancelada!"); //caso clicar em não/no, a operação será cancelada
            }   


        }
        else{
            JOptionPane.showMessageDialog(null, "Selecione uma venda para excluir."); //caso for pressionado o botão de excluir e nenhuma venda for selecionada, esta mensagem aparecerá
        }
    }//GEN-LAST:event_bntExcluirActionPerformed
    
    
