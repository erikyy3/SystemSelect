    public void preencherTabela(List<Venda> vendas){ //método para preencher a tabela com os dados salvos no banco.
        String colunas[] = {"Id", "Cliente", "Data", "Item", "Preço"}; //separa as colunas da tabela swing
        String dados[][] = new String[vendas.size()][colunas.length]; //separa as linhas da tabela swing de acordo com o tamanho da lista dada no parâmetro do método.
        
        DateTimeFormatter formatter = DateTimeFormatter.ofPattern("dd/MM/yyyy HH:mm:ss");
        int i = 0;
        for(Venda v : vendas){
            dados[i] = new String[]{ //a cada venda 'v', seus dados são coletados e preenchidos na linha da tabela swing.
                String.valueOf(v.getId()),
                v.getCliente().getNome(),
                v.getData().format(formatter),
                v.getComputador().getNome(),
                String.valueOf(v.getComputador().getPreco())
            }; 
            i++;
        }
        
        DefaultTableModel model = new DefaultTableModel(dados, colunas);
        tblVenda.setModel(model);
    }
    
